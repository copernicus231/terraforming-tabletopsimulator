function bagGuids(bag)
    local guids = {}
    for i,a in ipairs(bag.getObjects()) do
        guids[#guids + 1] = a.guid
    end
    return guids
end

function containsList(obj,list)
    for i,a in pairs(list) do
        if a == obj then
            return true
        end
    end
    return false
end
function deployObjectsBag(bagGUID,config)
