    end
end
function removeCards(deckGUID,list,bagGUID)
    local deck = getObjectFromGUID(deckGUID)
    local bag = getObjectFromGUID(bagGUID)
    for _,name in pairs(list) do
        for _, containedObject in ipairs(deck.getObjects()) do
            if containedObject.name == name then
                bag.putObject(deck.takeObject({
                    index = containedObject.index
                }))
                break -- Stop iterating
            end
        end
    end
end

